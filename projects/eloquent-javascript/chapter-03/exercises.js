function isEven(num) {
  //base case if num is evem
  if(num === 0){
    return true;
    //base case if num is uneven false
  }else if(num === 1){
    return false;
    //if num is negative
  }else if(num < 0){
    //return funct called with neg number
    return isEven(-num);
  }
  //recursive case
  return isEven(num - 2);
}

